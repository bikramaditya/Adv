namespace JellyFish
{
    public class Coords
    {
        public int X;
        public int Y;
        public int W;
        public int H;

        public Coords(int x, int y, int w, int h)
        {
            this.X = x;
            this.Y = y;
            this.W = w;
            this.H = h;
        }
    }
}